    <h4>Types of RPO models:</h4>
    <p>The RPO assistance is further divided into three types based on the nature of help required by the companies who
        outsource their recruiting process. We are enlisting these types below for your reference,</p>

    <h6>1- On-demand RPO</h6>
    <p>This type of RPO solution involves engaging outsourced recruiters to join your talent acquisition team for a specific
        project. It is a contract-based engagement with a third-party service provider who is aware of the company's hiring
        policies.
    </p>
    <h6>2- End-to-End RPO</h6>
    <p>This RPO solution is generally used for a streamlined market, country or region. As in, this hiring mechanism is
        extensive and can focus on recruitment for any business division or job role. Hence, the end-to-end RPO initiative
        encompasses all the hiring requirements of a company.
    </p>
    <h6>3- Project RPO</h6>
    <p>This type of RPO allows the company to outsource the talent acquisition process to a team of professionals who would
        hire the best talent. This service is usually an ad-hoc request which should be accomplished within a designated
        timeframe. It includes,
    <ul style="list-style-type: disc;">
        <li>Hiring extra manpower to mitigate excessive workload during busy seasons</li>
        <li>Launching a new team or sub service line and</li>
        <li>Performing other just-in-time requirements</li>
    </ul>
    Finally, an outsourced provider is also used to build a contingent workforce. Therefore, this initiative is more like a
    strategic partnership between your company and the third-party provider. The resultant of this partnership also brings
    value added benefits.
    </p>

