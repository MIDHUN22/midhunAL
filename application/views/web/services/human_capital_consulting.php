<h2>Human Capital Consulting</h2>
<p>
    Human capital consulting takes an individual who can mentor, manage, and coach others. Whether you are considering
    this as a career, or need a consultant, there are certain qualities that make a consultant most effective:
</p>
<ul style='list-style-type: disc;padding-left:10px;'>
    <li>The ability to adapt methods and practices in order to fit the specific needs of a project or a team of workers.
    </li>
    <li>The talent of establishing goals and ways to implement a plan to achieve the goals, as well as giving timeframes
        for when the goals should be complete.</li>
    <li>The ability to create and maintain strong relationships with clients, management teams, employees, and others in
        their professional circles.</li>
    <li>The determination to stay current on trends and other developments in the advisory and human resource
        industries.</li>
</ul>