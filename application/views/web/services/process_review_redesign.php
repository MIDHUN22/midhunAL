<h2>Process Review, Redesign</h2>
<p>
    HR process re-engineering is all about optimization and continuous improvement. It is the fundamental rethinking and
    radical redesign that leads to an increase in productivity. The aim of process re-engineering is to improve the
    level of process performance in order to achieve significant and sustainable improvements in the performance of your
    HR department. Through HR process re-engineering, HR professionals can re-think how they operate and find better or
    improved ways to provide HR services.
</p>
<h4>What are the benefits and ROI?</h4>
<p>There are many benefits of using this tool and some of the expected outcomes include:</p>
<ul style='list-style-type: disc;padding-left:10px;'>
    <li>Reduced Cost – There are a number of areas where your company can achieve cost reduction. This includes
        time-savings and a reduction of error rates. In addition, when a self-service option is provided to employees
        there is potential saving in cost by reducing manpower requirements.</li>
    <li>Increase in departmental efficiency – Process re-engineering will drive the automation of a lot of repetitive
        elements and therefore you will be able to remove duplicate steps and parallel processing. The improvement will
        give the HR team the capacity to focus on value-added activities moving away from transactional to strategic HR.
    </li>
    <li>Employee engagement – By optimizing your HR processes you can make your employees happier as issues that they
        are facing can be handled faster through automation.</li>
    <li>Improvement in customer service levels – Process re-engineering ensures that the quality of HR services and
        information is delivered efficiently and consistently across the business.</li>
</ul>
<h4>When is HR process re-engineering the answer?</h4>
<p>
    Knowing when and how to innovate is a challenge for every HR function. A close and critical assessment of HR
    processes will provide clarity and focus on whether a re-design of an HR process is required. Some of the red flags
    include:
</p>
<ul style='list-style-type: disc;padding-left:10px;'>
    <li>The presence of non value-added activities e.g. using both excel/spreadsheets and I.T for tracking purposes</li>
    <li>Duplication in steps for process completion e.g. multiple data entry</li>
    <li>Deviations in the process e.g. difference in requirements for the same service</li>
    <li>SLAs are not adhered to</li>
    <li>Disconnect between end-user requirements and the services provided</li>
</ul>